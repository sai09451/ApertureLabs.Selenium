namespace ApertureLabs.Selenium.Components.Kendo.KDatePicker
{
    /// <summary>
    /// The navigation depth of a <see cref="KDatePickerComponent{T}"/>.
    /// </summary>
    public enum Depth
    {
        /// <summary>
        /// Shows the days of the month.
        /// </summary>
        Month = 0,

        /// <summary>
        /// Shows the months of the year.
        /// </summary>
        Year = 1,

        /// <summary>
        /// Shows the years of the decade.
        /// </summary>
        Decade = 2,

        /// <summary>
        /// Shows the decades of the century.
        /// </summary>
        Century = 3
    }
}
